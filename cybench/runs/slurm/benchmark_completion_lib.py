@dataclass(frozen=True)
class JobRow:
    crop: str
    country: str
    model: str
    framework: str
    hp_search: str
    feature_design: str
    needs_gpu: str

    def to_line(self) -> str:
        return (
            f"{self.crop} {self.country} {self.model} {self.framework} "
            f"{self.hp_search} {self.feature_design} {self.needs_gpu}"
        )


@dataclass(frozen=True)
class JobAssessment:
    job: JobRow
    n_years: int
    screening_ok: bool
    screening_reason: str
    walk_forward_ok: bool
    walk_forward_reason: str
    blocked: bool
    block_reason: str

    @property
    def needs_screening(self) -> bool:
        return not self.blocked and not self.screening_ok

    @property
    def needs_walk_forward(self) -> bool:
        return (
            not self.blocked
            and self.screening_ok
            and not self.walk_forward_ok
        )


def read_manifest(path: Path) -> list[JobRow]:
    rows: list[JobRow] = []
    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.split("#", 1)[0].strip()
        if not line:
            continue
        parts = line.split()
        if len(parts) != 7:
            raise ValueError(f"Expected 7 columns in {path}: {line!r}")
        rows.append(
            JobRow(
                crop=parts[0],
                country=parts[1],
                model=parts[2],
                framework=parts[3],
                hp_search=parts[4],
                feature_design=parts[5],
                needs_gpu=parts[6],
            )
        )
    return rows


def write_manifest(path: Path, jobs: list[JobRow], *, header: str | None = None) -> None:
    lines = [
        header
        or "# crop country model framework hp_search feature_design needs_gpu",
        "# Generated by orchestrate_benchmark_complete.py — incomplete jobs only.",
    ]
    lines.extend(job.to_line() for job in jobs)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def target_walk_forward_seeds(*, base_seed: int, total_repetitions: int) -> list[int]:
    if total_repetitions < 1:
        raise ValueError(f"total_repetitions must be >= 1, got {total_repetitions}")
    return list(range(base_seed, base_seed + total_repetitions))


def walk_forward_seeds_for_job(
    job: JobRow,
    *,
    baselines_dir: Path,
    horizon_tag_value: str,
    repo_root: Path,
    base_seed: int,
    total_repetitions: int,
    resume: bool,
) -> list[int]:
    """Seeds to run for one job (full target list, or missing only when resume)."""
    targets = target_walk_forward_seeds(
        base_seed=base_seed, total_repetitions=total_repetitions
    )
    if not resume:
        return targets
    run_dir = _latest_run(
        baselines_dir,
        crop=job.crop,
        country=job.country,
        model_slug=job.model,
        phase="walk_forward",
        horizon_tag_value=horizon_tag_value,
        repo_root=repo_root,
    )
    if run_dir is None:
        return targets
    existing = set(discover_run_seeds(run_dir))
    return [seed for seed in targets if seed not in existing]


def expand_walk_forward_manifest_lines(
    jobs: list[JobRow],
    *,
    baselines_dir: Path,
    horizon: str,
    repo_root: Path,
    base_seed: int = 42,
    total_repetitions: int = 1,
    resume: bool = False,
    per_seed_for_gpu: bool = True,
) -> list[str]:
    """Expand manifest lines. GPU rows become one SLURM task per seed; CPU/naive stay bundled."""
    hz_tag = horizon_tag(horizon)
    lines: list[str] = []
    for job in jobs:
        if per_seed_for_gpu and job.needs_gpu == "yes":
            seeds = walk_forward_seeds_for_job(
                job,
                baselines_dir=baselines_dir,
                horizon_tag_value=hz_tag,
                repo_root=repo_root,
                base_seed=base_seed,
                total_repetitions=total_repetitions,
                resume=resume,
            )
            for seed in seeds:
                lines.append(f"{job.to_line()} {seed}")
            continue

        if resume:
            seeds = walk_forward_seeds_for_job(
                job,
                baselines_dir=baselines_dir,
                horizon_tag_value=hz_tag,
                repo_root=repo_root,
                base_seed=base_seed,
                total_repetitions=total_repetitions,
                resume=True,
            )
            if not seeds:
                continue
        lines.append(job.to_line())
    return lines


def horizon_tag(end_of_sequence: str) -> str:
    return prediction_horizon_tag(normalize_horizon(end_of_sequence))


def model_run_name(model_slug: str, *, repo_root: Path) -> str:
    cfg_path = repo_root / "cybench" / "conf" / "model" / f"{model_slug}.yaml"
    if not cfg_path.is_file():
        return model_slug
    cfg = OmegaConf.load(cfg_path)
    name = OmegaConf.select(cfg, "name", default=model_slug)
    return str(name)


def load_yield_years(
    crop: str,
    country: str,
    *,
    data_dir: Path | None = None,
    min_year: int = DEFAULT_MIN_YEAR,
    max_year: int = DEFAULT_MAX_YEAR,
) -> set[int]:
    data_dir = Path(data_dir or config.PATH_DATA_DIR)
    path = data_dir / crop / country / f"yield_{crop}_{country}.csv"
    if not path.is_file():
        return set()
    years: set[int] = set()
    with path.open(encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        year_col = "harvest_year" if "harvest_year" in (reader.fieldnames or []) else "year"
        for row in reader:
            raw = row.get(year_col)
            if raw is None or raw == "":
                continue
            year = int(float(raw))
            if min_year <= year <= max_year:
                years.add(year)
    return years


def check_screening_years(years: set[int]) -> tuple[bool, str]:
    if not years:
        return False, "no yield years in dataset window"
    try:
        train, val, test = get_screening_partitions(
            default_screening_validation_cfg(), years, seed=42
        )
    except (AssertionError, ValueError) as exc:
        return False, str(exc)
    if not train:
        return False, "no train years after screening split"
    return True, f"train={len(train)} val={len(val)} test={len(test)}"


def _latest_run(
    baselines_dir: Path,
    *,
    crop: str,
    country: str,
    model_slug: str,
    phase: str,
    horizon_tag_value: str,
    repo_root: Path,
) -> Path | None:
    runs = discover_benchmark_runs(
        baselines_dir,
        phase=phase,
        horizon=horizon_tag_value,
        latest_only=True,
        allow_missing=True,
    )
    expected_name = model_run_name(model_slug, repo_root=repo_root)
    crop_key = crop.casefold()
    country_key = country.casefold()
    for run in runs:
        if run.crop.casefold() != crop_key:
            continue
        if run.country.casefold() != country_key:
            continue
        if run.model in {model_slug, expected_name}:
            return run.path
    return None


def screening_complete(
    baselines_dir: Path,
    job: JobRow,
    *,
    horizon_tag_value: str,
    repo_root: Path,
) -> tuple[bool, str]:
    run_dir = _latest_run(
        baselines_dir,
        crop=job.crop,
        country=job.country,
        model_slug=job.model,
        phase="screening",
        horizon_tag_value=horizon_tag_value,
        repo_root=repo_root,
    )
    if run_dir is None:
        return False, "no screening run"
    frozen = next(run_dir.rglob("optimal_model.yaml"), None)
    if frozen is None:
        return False, f"screening run missing optimal_model.yaml ({run_dir.name})"
    return True, run_dir.name


def walk_forward_complete(
    baselines_dir: Path,
    job: JobRow,
    *,
    horizon_tag_value: str,
    repo_root: Path,
    base_seed: int = 42,
    total_repetitions: int | None = None,
    data_dir: Path | None = None,
    min_year: int = DEFAULT_MIN_YEAR,
    max_year: int = DEFAULT_MAX_YEAR,
) -> tuple[bool, str]:
    run_dir = _latest_run(
        baselines_dir,
        crop=job.crop,
        country=job.country,
        model_slug=job.model,
        phase="walk_forward",
        horizon_tag_value=horizon_tag_value,
        repo_root=repo_root,
    )
    if run_dir is None:
        return False, "no walk-forward run"
    existing = set(discover_run_seeds(run_dir))
    if not existing:
        return False, "no walk-forward predictions"
    if total_repetitions is not None:
        targets = target_walk_forward_seeds(
            base_seed=base_seed, total_repetitions=total_repetitions
        )
        missing = [seed for seed in targets if seed not in existing]
        if missing:
            return False, f"missing walk-forward seeds {missing} in {run_dir.name}"
        check_seeds = targets
    else:
        check_seeds = sorted(existing)
    dataset_years = load_yield_years(
        job.crop,
        job.country,
        data_dir=data_dir,
        min_year=min_year,
        max_year=max_year,
    )
    expected_years = expected_walk_forward_test_years(dataset_years)
    if not expected_years:
        return False, "no walk-forward test years for dataset window"
    missing_years = walk_forward_missing_years(
        run_dir,
        expected_years=expected_years,
        seeds=check_seeds,
    )
    if missing_years:
        detail = ", ".join(
            f"seed {seed} missing {years}" for seed, years in sorted(missing_years.items())
        )
        return False, f"incomplete walk-forward years in {run_dir.name}: {detail}"
    for seed in check_seeds:
        try:
            load_pooled_predictions(run_dir, model_slug=job.model, seed=seed)
        except ValueError as exc:
            return False, str(exc)
    return True, run_dir.name


def assess_job(
    job: JobRow,
    *,
    baselines_dir: Path,
    horizon: str,
    repo_root: Path,
    data_dir: Path | None = None,
    min_year: int = DEFAULT_MIN_YEAR,
    max_year: int = DEFAULT_MAX_YEAR,
    wf_repetitions: int | None = None,
    wf_base_seed: int = 42,
) -> JobAssessment:
    hz_tag = horizon_tag(horizon)
    years = load_yield_years(
        job.crop,
        job.country,
        data_dir=data_dir,
        min_year=min_year,
        max_year=max_year,
    )
    ok_years, years_reason = check_screening_years(years)
    if not ok_years:
        return JobAssessment(
            job=job,
            n_years=len(years),
            screening_ok=False,
            screening_reason=years_reason,
            walk_forward_ok=False,
            walk_forward_reason="blocked",
            blocked=True,
            block_reason=years_reason,
        )

    scr_ok, scr_reason = screening_complete(
        baselines_dir, job, horizon_tag_value=hz_tag, repo_root=repo_root
    )
    wf_ok, wf_reason = walk_forward_complete(
        baselines_dir,
        job,
        horizon_tag_value=hz_tag,
        repo_root=repo_root,
        base_seed=wf_base_seed,
        total_repetitions=wf_repetitions,
        data_dir=data_dir,
        min_year=min_year,
        max_year=max_year,
    )
    if not scr_ok:
        wf_reason = "waiting for screening"
    return JobAssessment(
        job=job,
        n_years=len(years),
        screening_ok=scr_ok,
        screening_reason=scr_reason,
        walk_forward_ok=wf_ok,
        walk_forward_reason=wf_reason,
        blocked=False,
        block_reason="",
    )


def assess_manifest(
    jobs: list[JobRow],
    *,
    baselines_dir: Path,
    horizon: str,
    repo_root: Path,
    data_dir: Path | None = None,
    wf_repetitions: int | None = None,
    wf_base_seed: int = 42,
) -> list[JobAssessment]:
    return [
        assess_job(
            job,
            baselines_dir=baselines_dir,
            horizon=horizon,
            repo_root=repo_root,
            data_dir=data_dir,
            wf_repetitions=wf_repetitions,
            wf_base_seed=wf_base_seed,
        )
        for job in jobs
    ]


def filter_jobs_by_models(
    jobs: list[JobRow],
    models: list[str] | None,
) -> list[JobRow]:
    if not models:
        return jobs
    allowed = {model.lower() for model in models}
    return [job for job in jobs if job.model.lower() in allowed]


def filter_jobs_cpu_only(jobs: list[JobRow]) -> list[JobRow]:
    """Keep naive + CPU-tabular rows (needs_gpu=no); exclude torch / TabPFN / TabDPT."""
    return [job for job in jobs if job.needs_gpu == "no"]


def job_row_key(job: JobRow) -> tuple[str, str, str, str, str, str, str]:
    return (
        job.crop,
        job.country,
        job.model,
        job.framework,
        job.hp_search,
        job.feature_design,
        job.needs_gpu,
    )


def supplement_jobs_for_models(
    *,
    country: str,
    models: list[str],
    repo_root: Path,
    data_dir: Path | None = None,
    models_path: Path | None = None,
    horizon: str | None = None,
) -> list[JobRow]:
    """Build job rows from current models.txt + on-disk data (ignores cached manifests)."""
    from cybench.runs.slurm.generate_job_manifest import MODELS_FILE, build_jobs

    path = models_path or (repo_root / "cybench" / "runs" / "slurm" / MODELS_FILE.name)
    if not path.is_file():
        path = MODELS_FILE
    return build_jobs(
        crops=["maize", "wheat"],
        countries=[country],
        models_path=path,
        data_dir=data_dir,
        model_filter=models,
        horizon=horizon,
    )


def merge_jobs_for_models(
    jobs: list[JobRow],
    *,
    country: str,
    models: list[str],
    repo_root: Path,
    data_dir: Path | None = None,
    models_path: Path | None = None,
) -> tuple[list[JobRow], bool]:
    """Filter to ``models`` and add any rows missing from a stale cached manifest."""
    filtered = filter_jobs_by_models(jobs, models)
    fresh = supplement_jobs_for_models(
        country=country,
        models=models,
        repo_root=repo_root,
        data_dir=data_dir,
        models_path=models_path,
    )
    if not fresh and not filtered:
        return [], False

    merged: dict[tuple[str, str, str, str, str, str, str], JobRow] = {
        job_row_key(job): job for job in filtered
    }
    supplemented = False
    for job in fresh:
        key = job_row_key(job)
        if key not in merged:
            supplemented = True
        merged[key] = job
    return list(merged.values()), supplemented


def jobs_for_phase(
    assessments: list[JobAssessment],
    phase: str,
    *,
    force_rerun: bool = False,
) -> list[JobRow]:
    if phase == "screening":
        if force_rerun:
            return [a.job for a in assessments if not a.blocked]
        return [a.job for a in assessments if a.needs_screening]
    if phase == "walk_forward":
        if force_rerun:
            return [
                a.job
                for a in assessments
                if not a.blocked and a.screening_ok
            ]
        return [a.job for a in assessments if a.needs_walk_forward]
    if phase == "all":
        # Screening retries first; walk-forward-only rows are included too.
        out: list[JobRow] = []
        seen: set[tuple[str, str, str, str, str, str, str]] = set()
        for assessment in assessments:
            include = (
                (not assessment.blocked)
                if force_rerun
                else (assessment.needs_screening or assessment.needs_walk_forward)
            )
            for job in ([assessment.job] if include else []):
                key = (
                    job.crop,
                    job.country,
                    job.model,
                    job.framework,
                    job.hp_search,
                    job.feature_design,
                    job.needs_gpu,
                )
                if key not in seen:
                    seen.add(key)
                    out.append(job)
        return out
    raise ValueError(f"Unknown phase: {phase!r}")


def split_manifest_groups(jobs: list[JobRow]) -> dict[str, list[JobRow]]:
    cpu: list[JobRow] = []
    naive: list[JobRow] = []
    gpu: list[JobRow] = []
    for job in jobs:
        if job.hp_search == "no" and job.feature_design == "no" and job.needs_gpu == "no":
            naive.append(job)
        elif job.needs_gpu == "yes":
            gpu.append(job)
        elif job.feature_design == "yes":
            cpu.append(job)
    return {"cpu": cpu, "naive": naive, "gpu": gpu}


def default_output_root(repo_root: Path) -> Path:
    env = os.environ.get("CYBENCH_OUTPUT_ROOT")
    if env:
        return Path(env)
    if DEFAULT_LUSTRE_OUTPUT.is_dir():
        return DEFAULT_LUSTRE_OUTPUT
    return repo_root.parent / "output"


def filter_jobs_by_country(jobs: list[JobRow], country: str) -> list[JobRow]:
    cc = country.upper()
    return [job for job in jobs if job.country.upper() == cc]


def expand_target_batches(
    *,
    batch: str | None,
    country: str | None,
    horizons: list[str],
    version: int,
) -> list[tuple[str, str]]:
    """Return (batch_folder_name, slurm_horizon_value) pairs."""
    if not horizons:
        horizons = ["eos"]

    parsed = parse_batch_name(batch) if batch else None
    cc = (country or (parsed[0] if parsed else None) or "").upper()
    ver = version if country or not parsed else parsed[2]

    if parsed and len(horizons) > 1:
        cc = parsed[0]
        ver = version if country else parsed[2]
        return [
            (batch_name(cc, normalize_horizon(h), ver), normalize_horizon(h))
            for h in horizons
        ]

    if batch:
        hz = normalize_horizon(horizons[0])
        return [(batch, hz)]

    if cc:
        return [
            (batch_name(cc, normalize_horizon(h), ver), normalize_horizon(h))
            for h in horizons
        ]

    raise ValueError("Provide --batch or --country")


def discover_batches_on_disk(
    output_root: Path,
    *,
    horizons: list[str] | None = None,
    version: int | None = None,
) -> list[tuple[str, str]]:
    """Return ``(batch_folder_name, slurm_horizon)`` for existing output directories."""
    if not output_root.is_dir():
        return []
    wanted_hz: set[str] | None = None
    if horizons is not None:
        wanted_hz = {horizon_batch_suffix(normalize_horizon(h)) for h in horizons}

    found: list[tuple[str, str]] = []
    for entry in sorted(output_root.iterdir()):
        if not entry.is_dir():
            continue
        parsed = parse_batch_name(entry.name)
        if parsed is None:
            continue
        _cc, batch_hz, ver = parsed
        if version is not None and ver != version:
            continue
        if wanted_hz is not None and batch_hz not in wanted_hz:
            continue
        slurm_hz = batch_suffix_to_horizon(batch_hz)
        found.append((entry.name, slurm_hz))
    return found


def countries_from_shared_manifest(repo_root: Path) -> list[str]:
    shared = repo_root / "cybench" / "runs" / "slurm" / "benchmark_jobs.txt"
    if not shared.is_file():
        return []
    return sorted({job.country.upper() for job in read_manifest(shared)})


def resolve_country_list(
    *,
    all_countries: bool,
    countries: list[str] | None,
    country: str | None,
    repo_root: Path,
    data_dir: Path | None,
    prefer_output: bool = True,
    output_root: Path | None = None,
) -> list[str]:
    if countries:
        return sorted({c.upper() for c in countries})
    if country:
        return [country.upper()]
    if not all_countries:
        return []

    found: set[str] = set()
    root = output_root or default_output_root(repo_root)
    if prefer_output and root.is_dir():
        for entry in root.iterdir():
            parsed = parse_batch_name(entry.name)
            if parsed:
                found.add(parsed[0])
    found.update(countries_from_shared_manifest(repo_root))
    if not found:
        found.update(countries_with_data(data_dir))
    return sorted(found)


def expand_all_country_targets(
    *,
    countries: list[str],
    horizons: list[str],
    version: int,
) -> list[tuple[str, str]]:
    """Expand many countries × horizons into batch targets."""
    targets: list[tuple[str, str]] = []
    for cc in countries:
        targets.extend(
            expand_target_batches(
                batch=None,
                country=cc,
                horizons=horizons,
                version=version,
            )
        )
    return targets


def ensure_manifest(
    *,
    batch: str,
    repo_root: Path,
    manifest_path: Path | None,
    write_cache: bool = True,
) -> tuple[Path, list[JobRow], str]:
    """Resolve or build the expected job list for a batch.

    Returns (manifest_path, jobs, source_description).
    """
    slurm_dir = repo_root / "cybench" / "runs" / "slurm"
    manifests_parent = slurm_dir / "manifests"
    manifest_root = resolve_case_insensitive_child(manifests_parent, batch)
    if manifest_root is None:
        manifest_root = manifests_parent / batch
    if manifest_path and manifest_path.is_file():
        jobs = read_manifest(manifest_path)
        return manifest_path, jobs, f"explicit {manifest_path}"

    default = manifest_root / "benchmark_jobs.txt"
    if default.is_file():
        jobs = read_manifest(default)
        return default, jobs, str(default)

    parsed = parse_batch_name(batch)
    country = parsed[0] if parsed else None

    shared = slurm_dir / "benchmark_jobs.txt"
    if shared.is_file() and country:
        jobs = filter_jobs_by_country(read_manifest(shared), country)
        if jobs:
            if write_cache:
                manifest_root.mkdir(parents=True, exist_ok=True)
                write_manifest(default, jobs)
            return default, jobs, f"filtered {shared} for {country}"

    if country:
        from cybench.runs.slurm.generate_job_manifest import generate

        models = slurm_dir / "models.txt"
        manifest_root.mkdir(parents=True, exist_ok=True)
        horizon = None
        if parsed:
            horizon = batch_suffix_to_horizon(parsed[1])
        generate(
            crops=["maize", "wheat"],
            countries=[country],
            models_path=models,
            output=default,
            horizon=horizon,
        )
        jobs = read_manifest(default)
        if jobs:
            return default, jobs, f"generated for {country} via generate_job_manifest.py"

    raise FileNotFoundError(
        f"No manifest for batch {batch!r}. Tried {default}, shared {shared}, "
        f"and generate_job_manifest.py (need batch name baselines_CC_eos_v1, "
        f"baselines_CC_qtr_v1, or --manifest)."
    )


def resolve_paths(
    *,
    batch: str,
    repo_root: Path,
    baselines_dir: Path | None,
    manifest_path: Path | None,
    output_root: Path | None,
) -> tuple[Path, Path, Path, Path, list[JobRow], str, str | None]:
    slurm_dir = repo_root / "cybench" / "runs" / "slurm"
    manifests_parent = slurm_dir / "manifests"
    manifest_root = resolve_case_insensitive_child(manifests_parent, batch)
    if manifest_root is None:
        manifest_root = manifests_parent / batch
    root = output_root or default_output_root(repo_root)
    alias_note: str | None = None
    if baselines_dir is None:
        baselines_dir, alias_note = resolve_batch_dir(root, batch)
    manifest, jobs, source = ensure_manifest(
        batch=batch,
        repo_root=repo_root,
        manifest_path=manifest_path,
    )
    return manifest, baselines_dir, manifest_root, root, jobs, source, alias_note
