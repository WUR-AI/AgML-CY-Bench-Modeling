"""Assess benchmark job completeness and build retry manifests."""

from __future__ import annotations

import csv
import os
import re
from dataclasses import dataclass
from pathlib import Path
from types import SimpleNamespace

from omegaconf import OmegaConf

import cybench.config as config
from cybench.runs.analysis.benchmark_run_catalog import discover_benchmark_runs
from cybench.runs.analysis.collect_walk_forward_results import load_pooled_predictions
from cybench.runs.slurm.benchmark_submit_lib import batch_name, normalize_horizon
from cybench.util.prediction_horizon import prediction_horizon_tag
from cybench.util.validation import get_screening_partitions

SCREENING_TEST_YEARS = "5-last"
SCREENING_VAL_YEARS = "2-last"
DEFAULT_MIN_YEAR = 2000
DEFAULT_MAX_YEAR = 2024
DEFAULT_LUSTRE_OUTPUT = Path("/lustre/backup/SHARED/AIN/agml/output")

_BATCH_RE = re.compile(
    r"^baselines_(?P<country>[A-Za-z]{2})_(?P<batch_hz>eos|mid)_v(?P<version>\d+)$"
)


@dataclass(frozen=True)
class JobRow:
    crop: str
    country: str
    model: str
    framework: str
    hp_search: str
    feature_design: str
    needs_gpu: str

    def to_line(self) -> str:
        return (
            f"{self.crop} {self.country} {self.model} {self.framework} "
            f"{self.hp_search} {self.feature_design} {self.needs_gpu}"
        )


@dataclass(frozen=True)
class JobAssessment:
    job: JobRow
    n_years: int
    screening_ok: bool
    screening_reason: str
    walk_forward_ok: bool
    walk_forward_reason: str
    blocked: bool
    block_reason: str

    @property
    def needs_screening(self) -> bool:
        return not self.blocked and not self.screening_ok

    @property
    def needs_walk_forward(self) -> bool:
        return (
            not self.blocked
            and self.screening_ok
            and not self.walk_forward_ok
        )


def read_manifest(path: Path) -> list[JobRow]:
    rows: list[JobRow] = []
    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.split("#", 1)[0].strip()
        if not line:
            continue
        parts = line.split()
        if len(parts) != 7:
            raise ValueError(f"Expected 7 columns in {path}: {line!r}")
        rows.append(
            JobRow(
                crop=parts[0],
                country=parts[1],
                model=parts[2],
                framework=parts[3],
                hp_search=parts[4],
                feature_design=parts[5],
                needs_gpu=parts[6],
            )
        )
    return rows


def write_manifest(path: Path, jobs: list[JobRow], *, header: str | None = None) -> None:
    lines = [
        header
        or "# crop country model framework hp_search feature_design needs_gpu",
        "# Generated by orchestrate_benchmark_complete.py — incomplete jobs only.",
    ]
    lines.extend(job.to_line() for job in jobs)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def horizon_tag(end_of_sequence: str) -> str:
    return prediction_horizon_tag(normalize_horizon(end_of_sequence))


def model_run_name(model_slug: str, *, repo_root: Path) -> str:
    cfg_path = repo_root / "cybench" / "conf" / "model" / f"{model_slug}.yaml"
    if not cfg_path.is_file():
        return model_slug
    cfg = OmegaConf.load(cfg_path)
    name = OmegaConf.select(cfg, "name", default=model_slug)
    return str(name)


def load_yield_years(
    crop: str,
    country: str,
    *,
    data_dir: Path | None = None,
    min_year: int = DEFAULT_MIN_YEAR,
    max_year: int = DEFAULT_MAX_YEAR,
) -> set[int]:
    data_dir = Path(data_dir or config.PATH_DATA_DIR)
    path = data_dir / crop / country / f"yield_{crop}_{country}.csv"
    if not path.is_file():
        return set()
    years: set[int] = set()
    with path.open(encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        year_col = "harvest_year" if "harvest_year" in (reader.fieldnames or []) else "year"
        for row in reader:
            raw = row.get(year_col)
            if raw is None or raw == "":
                continue
            year = int(float(raw))
            if min_year <= year <= max_year:
                years.add(year)
    return years


def check_screening_years(years: set[int]) -> tuple[bool, str]:
    if not years:
        return False, "no yield years in dataset window"
    cfg = SimpleNamespace(
        name="screening",
        test_years=SCREENING_TEST_YEARS,
        val_years=SCREENING_VAL_YEARS,
    )
    try:
        train, val, test = get_screening_partitions(cfg, years, seed=42)
    except (AssertionError, ValueError) as exc:
        return False, str(exc)
    if not train:
        return False, "no train years after screening split"
    return True, f"train={len(train)} val={len(val)} test={len(test)}"


def _latest_run(
    baselines_dir: Path,
    *,
    crop: str,
    country: str,
    model_slug: str,
    phase: str,
    horizon_tag_value: str,
    repo_root: Path,
) -> Path | None:
    runs = discover_benchmark_runs(
        baselines_dir,
        phase=phase,
        horizon=horizon_tag_value,
        latest_only=True,
    )
    expected_name = model_run_name(model_slug, repo_root=repo_root)
    for run in runs:
        if run.crop != crop or run.country != country:
            continue
        if run.model in {model_slug, expected_name}:
            return run.path
    return None


def screening_complete(
    baselines_dir: Path,
    job: JobRow,
    *,
    horizon_tag_value: str,
    repo_root: Path,
) -> tuple[bool, str]:
    run_dir = _latest_run(
        baselines_dir,
        crop=job.crop,
        country=job.country,
        model_slug=job.model,
        phase="screening",
        horizon_tag_value=horizon_tag_value,
        repo_root=repo_root,
    )
    if run_dir is None:
        return False, "no screening run"
    frozen = next(run_dir.rglob("optimal_model.yaml"), None)
    if frozen is None:
        return False, f"screening run missing optimal_model.yaml ({run_dir.name})"
    return True, run_dir.name


def walk_forward_complete(
    baselines_dir: Path,
    job: JobRow,
    *,
    horizon_tag_value: str,
    repo_root: Path,
) -> tuple[bool, str]:
    run_dir = _latest_run(
        baselines_dir,
        crop=job.crop,
        country=job.country,
        model_slug=job.model,
        phase="walk_forward",
        horizon_tag_value=horizon_tag_value,
        repo_root=repo_root,
    )
    if run_dir is None:
        return False, "no walk-forward run"
    try:
        load_pooled_predictions(run_dir, model_slug=job.model)
    except ValueError as exc:
        return False, str(exc)
    return True, run_dir.name


def assess_job(
    job: JobRow,
    *,
    baselines_dir: Path,
    horizon: str,
    repo_root: Path,
    data_dir: Path | None = None,
    min_year: int = DEFAULT_MIN_YEAR,
    max_year: int = DEFAULT_MAX_YEAR,
) -> JobAssessment:
    hz_tag = horizon_tag(horizon)
    years = load_yield_years(
        job.crop,
        job.country,
        data_dir=data_dir,
        min_year=min_year,
        max_year=max_year,
    )
    ok_years, years_reason = check_screening_years(years)
    if not ok_years:
        return JobAssessment(
            job=job,
            n_years=len(years),
            screening_ok=False,
            screening_reason=years_reason,
            walk_forward_ok=False,
            walk_forward_reason="blocked",
            blocked=True,
            block_reason=years_reason,
        )

    scr_ok, scr_reason = screening_complete(
        baselines_dir, job, horizon_tag_value=hz_tag, repo_root=repo_root
    )
    wf_ok, wf_reason = walk_forward_complete(
        baselines_dir, job, horizon_tag_value=hz_tag, repo_root=repo_root
    )
    if not scr_ok:
        wf_reason = "waiting for screening"
    return JobAssessment(
        job=job,
        n_years=len(years),
        screening_ok=scr_ok,
        screening_reason=scr_reason,
        walk_forward_ok=wf_ok,
        walk_forward_reason=wf_reason,
        blocked=False,
        block_reason="",
    )


def assess_manifest(
    jobs: list[JobRow],
    *,
    baselines_dir: Path,
    horizon: str,
    repo_root: Path,
    data_dir: Path | None = None,
) -> list[JobAssessment]:
    return [
        assess_job(
            job,
            baselines_dir=baselines_dir,
            horizon=horizon,
            repo_root=repo_root,
            data_dir=data_dir,
        )
        for job in jobs
    ]


def jobs_for_phase(
    assessments: list[JobAssessment],
    phase: str,
) -> list[JobRow]:
    if phase == "screening":
        return [a.job for a in assessments if a.needs_screening]
    if phase == "walk_forward":
        return [a.job for a in assessments if a.needs_walk_forward]
    if phase == "all":
        # Screening retries first; walk-forward-only rows are included too.
        out: list[JobRow] = []
        seen: set[tuple[str, str, str, str, str, str, str]] = set()
        for assessment in assessments:
            for job in (
                [assessment.job]
                if assessment.needs_screening or assessment.needs_walk_forward
                else []
            ):
                key = (
                    job.crop,
                    job.country,
                    job.model,
                    job.framework,
                    job.hp_search,
                    job.feature_design,
                    job.needs_gpu,
                )
                if key not in seen:
                    seen.add(key)
                    out.append(job)
        return out
    raise ValueError(f"Unknown phase: {phase!r}")


def split_manifest_groups(jobs: list[JobRow]) -> dict[str, list[JobRow]]:
    cpu: list[JobRow] = []
    naive: list[JobRow] = []
    gpu: list[JobRow] = []
    for job in jobs:
        if job.hp_search == "no" and job.feature_design == "no" and job.needs_gpu == "no":
            naive.append(job)
        elif job.needs_gpu == "yes":
            gpu.append(job)
        elif job.feature_design == "yes":
            cpu.append(job)
    return {"cpu": cpu, "naive": naive, "gpu": gpu}


def parse_batch_name(name: str) -> tuple[str, str, int] | None:
    match = _BATCH_RE.match(name)
    if not match:
        return None
    return match.group("country").upper(), match.group("batch_hz"), int(match.group("version"))


def default_output_root(repo_root: Path) -> Path:
    env = os.environ.get("CYBENCH_OUTPUT_ROOT")
    if env:
        return Path(env)
    if DEFAULT_LUSTRE_OUTPUT.is_dir():
        return DEFAULT_LUSTRE_OUTPUT
    return repo_root.parent / "output"


def filter_jobs_by_country(jobs: list[JobRow], country: str) -> list[JobRow]:
    cc = country.upper()
    return [job for job in jobs if job.country.upper() == cc]


def expand_target_batches(
    *,
    batch: str | None,
    country: str | None,
    horizons: list[str],
    version: int,
) -> list[tuple[str, str]]:
    """Return (batch_folder_name, slurm_horizon_value) pairs."""
    if not horizons:
        horizons = ["eos"]

    parsed = parse_batch_name(batch) if batch else None
    cc = (country or (parsed[0] if parsed else None) or "").upper()
    ver = version if country or not parsed else parsed[2]

    if parsed and len(horizons) > 1:
        cc = parsed[0]
        ver = version if country else parsed[2]
        return [
            (batch_name(cc, normalize_horizon(h), ver), normalize_horizon(h))
            for h in horizons
        ]

    if batch:
        hz = normalize_horizon(horizons[0])
        return [(batch, hz)]

    if cc:
        return [
            (batch_name(cc, normalize_horizon(h), ver), normalize_horizon(h))
            for h in horizons
        ]

    raise ValueError("Provide --batch or --country")


def ensure_manifest(
    *,
    batch: str,
    repo_root: Path,
    manifest_path: Path | None,
    write_cache: bool = True,
) -> tuple[Path, list[JobRow], str]:
    """Resolve or build the expected job list for a batch.

    Returns (manifest_path, jobs, source_description).
    """
    slurm_dir = repo_root / "cybench" / "runs" / "slurm"
    manifest_root = slurm_dir / "manifests" / batch
    if manifest_path and manifest_path.is_file():
        jobs = read_manifest(manifest_path)
        return manifest_path, jobs, f"explicit {manifest_path}"

    default = manifest_root / "benchmark_jobs.txt"
    if default.is_file():
        jobs = read_manifest(default)
        return default, jobs, str(default)

    parsed = parse_batch_name(batch)
    country = parsed[0] if parsed else None

    shared = slurm_dir / "benchmark_jobs.txt"
    if shared.is_file() and country:
        jobs = filter_jobs_by_country(read_manifest(shared), country)
        if jobs:
            if write_cache:
                manifest_root.mkdir(parents=True, exist_ok=True)
                write_manifest(default, jobs)
            return default, jobs, f"filtered {shared} for {country}"

    if country:
        from cybench.runs.slurm.generate_job_manifest import generate

        models = slurm_dir / "models.txt"
        manifest_root.mkdir(parents=True, exist_ok=True)
        generate(
            crops=["maize", "wheat"],
            countries=[country],
            models_path=models,
            output=default,
        )
        jobs = read_manifest(default)
        if jobs:
            return default, jobs, f"generated for {country} via generate_job_manifest.py"

    raise FileNotFoundError(
        f"No manifest for batch {batch!r}. Tried {default}, shared {shared}, "
        f"and generate_job_manifest.py (need batch name baselines_CC_eos_v1 or --manifest)."
    )


def resolve_paths(
    *,
    batch: str,
    repo_root: Path,
    baselines_dir: Path | None,
    manifest_path: Path | None,
    output_root: Path | None,
) -> tuple[Path, Path, Path, Path, list[JobRow], str]:
    slurm_dir = repo_root / "cybench" / "runs" / "slurm"
    manifest_root = slurm_dir / "manifests" / batch
    root = output_root or default_output_root(repo_root)
    if baselines_dir is None:
        baselines_dir = root / batch
    manifest, jobs, source = ensure_manifest(
        batch=batch,
        repo_root=repo_root,
        manifest_path=manifest_path,
    )
    return manifest, baselines_dir, manifest_root, root, jobs, source
